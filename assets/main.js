function applyLanguage(lang) {
    if (lang === 'en') {
        const walker = document.createTreeWalker(document.body, NodeFilter.SHOW_TEXT, null, false);
        let node;
        while ((node = walker.nextNode())) {
            if (node.originalText) {
                node.nodeValue = node.nodeValue.replace(node.nodeValue.trim(), node.originalText);
            }
        }
    } else {
        const walker = document.createTreeWalker(document.body, NodeFilter.SHOW_TEXT, null, false);
        let node;
        while ((node = walker.nextNode())) {
            let original = node.originalText || node.nodeValue.trim().replace(/\s+/g, ' ');
            if(!node.originalText && original !== '') {
                node.originalText = original; 
            }
            if (original && i18nDict[original] && i18nDict[original][lang]) {
                node.nodeValue = node.nodeValue.replace(node.nodeValue.trim(), i18nDict[original][lang]);
            }
        }
    }
    
    $('input[placeholder], textarea[placeholder]').each(function() {
        let original = $(this).attr('data-orig-placeholder') || $(this).attr('placeholder');
        if(!$(this).attr('data-orig-placeholder')) $(this).attr('data-orig-placeholder', original);
        
        if (lang !== 'en' && i18nDict[original] && i18nDict[original][lang]) {
            $(this).attr('placeholder', i18nDict[original][lang]);
        } else if (lang === 'en') {
            $(this).attr('placeholder', original);
        }
    });

    $('html').attr('lang', lang);
    localStorage.setItem('mmodels_lang', lang);
    $('#lang-switcher').val(lang);
}

$(document).ready(function() {
    // Inject floating language selector
    if (!$('#lang-switcher').length) {
        $('body').append(`
            <div class="fixed bottom-6 right-6 z-50">
                <div class="relative">
                    <select id="lang-switcher" class="bg-white border border-gray-300 text-gray-900 text-xs rounded-full pl-4 pr-8 py-2 shadow-lg focus:outline-none focus:border-primary cursor-pointer font-bold uppercase tracking-widest appearance-none">
                        <option value="en">EN</option>
                        <option value="fr">FR</option>
                        <option value="ja">JA</option>
                    </select>
                    <i class="fas fa-globe absolute right-3 top-1/2 -translate-y-1/2 text-gray-400 pointer-events-none text-xs"></i>
                </div>
            </div>
        `);
        
        $('#lang-switcher').on('change', function() {
            applyLanguage($(this).val());
        });
    }

    // Populate mobile menu dynamically
    if ($('#mobile-menu').length && $.trim($('#mobile-menu').html()) === '') {
        const navLinks = [
            { name: 'Home', url: 'home.html' },
            { name: 'Video', url: 'video.html' },
            { name: 'Models', url: 'models.html' },
            { name: 'Our Clients', url: 'clients.html' },
            { name: 'Become a Model', url: 'become-a-model.html' },
            { name: 'Hire a Model', url: 'hire-a-model.html' },
            { name: "FAQ's", url: 'faq.html' },
            { name: 'Contact Us', url: 'contact.html' }
        ];
        let menuHtml = '<div class="pt-4 pb-2 space-y-1">';
        navLinks.forEach(link => {
            menuHtml += `<a href="${link.url}" class="block pl-6 pr-4 py-3 text-sm font-bold uppercase tracking-wider text-gray-600 hover:text-primary hover:bg-gray-50 border-l-4 border-transparent hover:border-primary transition">${link.name}</a>`;
        });
        menuHtml += '</div>';
        $('#mobile-menu').html(menuHtml);
    }

    // Initialize Localization
    const urlParams = new URLSearchParams(window.location.search);
    const urlLang = urlParams.get('lang');
    if (urlLang) {
        applyLanguage(urlLang);
        window.history.replaceState({}, '', window.location.pathname);
    } else {
        const savedLang = localStorage.getItem('mmodels_lang') || 'en';
        if(savedLang !== 'en') applyLanguage(savedLang);
    }

    // UI Logic
    $('#mobile-btn').click(function() {
        $('#mobile-menu').slideToggle(300);
    });

    $(window).scroll(function() {
        if ($(this).scrollTop() > 50) {
            $('#navbar').addClass('scrolled');
        } else {
            $('#navbar').removeClass('scrolled');
        }
    });

    if ($('#hero-content').length) {
        $('#hero-content').animate({'opacity': '1', 'margin-top': '0px'}, 1200);
    } else {
        if ($('#navbar').length && !$('#home').length) {
            $('#navbar').addClass('scrolled');
        }
    }

    $('.faq-question').click(function() {
        var parent = $(this).parent();
        if(parent.hasClass('active')) {
            parent.removeClass('active');
            parent.find('.faq-answer').slideUp(300);
        } else {
            $('.faq-item').removeClass('active');
            $('.faq-answer').slideUp(300);
            parent.addClass('active');
            parent.find('.faq-answer').slideDown(300);
        }
    });

    // Portfolio Filtering Logic
    $('.filter-btn').click(function(e) {
        e.preventDefault();
        $('.filter-btn').removeClass('text-primary border-b-2 border-primary').addClass('text-gray-400 hover:text-gray-900');
        $(this).addClass('text-primary border-b-2 border-primary').removeClass('text-gray-400 hover:text-gray-900');
        const target = $(this).data('target');
        $('.model-grid').addClass('hidden');
        $('#' + target).removeClass('hidden');
        $('#' + target + ' .model-card').css({opacity: 0, marginTop: '20px'}).animate({opacity: 1, marginTop: '0px'}, 400);
    });

    // Swiper Works Slider Initialization
    if ($('.worksSwiper').length) {
        new Swiper('.worksSwiper', {
            slidesPerView: 'auto',
            spaceBetween: 10,
            loop: true,
            speed: 8000,
            freeMode: true,
            allowTouchMove: true,
            autoplay: {
                delay: 0,
                disableOnInteraction: false,
            },
            breakpoints: {
                768: {
                    spaceBetween: 30,
                }
            }
        });
    }
});
